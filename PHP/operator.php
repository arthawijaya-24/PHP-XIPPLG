<?php

// Belajar Operator 

$nilai1 = 10;
$nilai2 = 20;


// + - * / %

$perjumlahan = $nilai1 + $nilai2;
echo '10 + 20 =' .  $perjumlahan . "<br>" ;

$perjumlahan = $nilai1 - $nilai2;
echo '10 - 20 =' . "$perjumlahan <br>";

$perjumlahan = $nilai1 * $nilai2;
echo '10 x 20 =' . "$perjumlahan <br>";

$perjumlahan = $nilai1 / $nilai2;
echo '10 : 20 =' . "$perjumlahan <br>" ;

$perjumlahan = $nilai1 % $nilai2;
echo '10 % 20 =' . "$perjumlahan <br>";
